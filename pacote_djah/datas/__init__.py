class Datas:
    def anoAtual():
        '''
        - > Função desenvolvida para sempre retornar o ano atual.
        return: retorna o ano atual.
        Created By: Djaaah
        '''
        from datetime import date
        ano_atual = date.today().year
        return ano_atual
    
    
    def mesAtual():
        '''
        - > Função desenvolvida para sempre retornar o ano atual.
        return: retorna o ano atual.
        Created By: Djaaah
        '''
        from datetime import date
        mes_atual = date.today().month
        return mes_atual
    
    
    def diaAtual():
        '''
        - > Função desenvolvida para sempre retornar o ano atual.
        return: retorna o ano atual.
        Created By: Djaaah
        '''
        from datetime import date
        dia_atual = date.today().day
        return dia_atual
    